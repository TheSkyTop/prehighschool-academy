function buildYearContent(): GeneratedContent {
  const generated: GeneratedContent = { lessons: [], questions: [], weeklyQuests: [] };

  for (let week = 3; week <= 52; week += 1) {
    dayPlan.forEach((plan, dayIndex) => {
      const topic = yearTopicPlan[plan.subject][week - 1];
      const lesson = lessonForTopic(plan.subject, topic, week);
      const questQuestionIds: string[] = [];

      if (plan.subject === "writing") {
        const question = writingQuestionForTopic(topic, week);
        generated.questions.push(question);
        questQuestionIds.push(question.id);
      } else {
        for (let index = 1; index <= 10; index += 1) {
          const question = questionForTopic(plan.subject, topic, week, index);
          generated.questions.push(question);
          questQuestionIds.push(question.id);
        }
      }

      generated.lessons.push(lesson);
      generated.weeklyQuests.push({
        id: `quest-week${week}-day${dayIndex + 1}`,
        day: plan.day,
        subject: plan.subject,
        title: `${plan.mission} Mission: ${topic}`,
        description: `Learn, demo, quiz, and review ${topic.toLowerCase()} with exam-style precision.`,
        lessonIds: [lesson.id],
        questionIds: questQuestionIds,
        rewardCoins: 100,
      });
    });
  }

  return generated;
}

const generatedYearContent = buildYearContent();
for (const legacyUnusedLessonId of ["lesson-math-patterns", "lesson-reading-nonfiction", "lesson-science-graphs"]) {
  const lessonIndex = lessons.findIndex((lesson) => lesson.id === legacyUnusedLessonId);
  if (lessonIndex > -1) lessons.splice(lessonIndex, 1);
}
lessons.push(...generatedYearContent.lessons);
questions.push(...generatedYearContent.questions);
weeklyQuests.push(...generatedYearContent.weeklyQuests);
const usedGeneratedQuestionIds = new Set(weeklyQuests.flatMap((quest) => quest.questionIds));
for (let questionIndex = questions.length - 1; questionIndex >= 0; questionIndex -= 1) {
  if (!usedGeneratedQuestionIds.has(questions[questionIndex].id)) questions.splice(questionIndex, 1);
}


export interface Question {
  id: string;
  subject: SubjectId;
  topic: string;
  subtopic: string;
  type: QuestionType;
  prompt: string;
  choices?: string[];
  correctAnswer: string | string[];
  explanation: string;
  difficulty: Difficulty;
  yearBand: "Year 4" | "Year 5" | "Year 6" | "Year 7 bridge";
  curriculumRefs: CurriculumRef[];
  examStyle: string[];
  skillType: string;
  estimatedTimeSeconds: number;
  calculatorAllowed: boolean;
  mistakeTags: string[];
}

